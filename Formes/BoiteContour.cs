namespace Formes
{
    public class BoiteContour
    {
        public readonly double x1;
        public readonly double y1;
        public readonly double x2;
        public readonly double y2;

        public BoiteContour(double x1, double y1, double x2, double y2)
        {
            this.x1 = x1;
            this.y1 = y1;
            this.x2 = x2;
            this.y2 = y2;
        }
    }
}